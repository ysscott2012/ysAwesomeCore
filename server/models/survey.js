var mongoose = require('mongoose');

var Survey = mongoose.model('Survey', {
  ID: {
    type: String
  },
  Question: {
    type: String
  },
  Choices: [
    String
  ],
  Section: {
    type: String
  },
  Type: {
    type: String
  },
  Answer: String,
  Note: String,
  ChoicesNote: [
    String
  ]
});



module.exports = {Survey};
